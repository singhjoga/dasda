package com.harbois.komrade.settings.credmgmt;

public class PleasantSettings {
	private String username;
	private String password;
	private String baseUrl;
	private String tokenPath;
	private String entriesPath;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getBaseUrl() {
		return baseUrl;
	}
	public void setBaseUrl(String baseUrl) {
		this.baseUrl = baseUrl;
	}
	public String getTokenPath() {
		return tokenPath;
	}
	public void setTokenPath(String tokenPath) {
		this.tokenPath = tokenPath;
	}
	public String getEntriesPath() {
		return entriesPath;
	}
	public void setEntriesPath(String entriesPath) {
		this.entriesPath = entriesPath;
	}
	
}
